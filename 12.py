async def command_new_handler(message: Message, state: FSMContext) -> None:
    """
    Обработчик команды /new.
    """
    await state.set_state(Form.question)
    await message.answer("Введите новый вопрос:")

@dp.message(Form.question)
async def process_question(message: Message, state: FSMContext) -> None:
    """
    Получает вопрос от пользователя.
    """
    await state.update_data(question=message.text)
    await state.set_state(Form.answers)
    await message.answer("Теперь введите ответы на вопрос (каждый ответ с новой строки):")

@dp.message(Form.answers)
async def process_answers(message: Message, state: FSMContext) -> None:
    """
    Получает ответы от пользователя и добавляет в базу знаний.
    """
    data = await state.get_data()
    question = data.get("question")
    answers = message.text.split('\n')
    # Выбираем первый ответ как основной
    answer = answers[0]
    knowledge_base[question] = answer

    await state.clear() # Сбрасываем состояние

    await message.answer(f"Вопрос '{question}' и ответ '{answer}' добавлены в базу знаний.")

@dp.message()
async def echo_handler(message: types.Message) -> None:
    """
    Обработчик всех текстовых сообщений.
    """
    query = message.text
    logging.info(f"Получен вопрос: {query}")

    # Поиск подходящего ответа
    answer = await find_best_match(query)

    if answer:
        await message.answer(answer)
    else:
        await message.answer("Я не знаю ответа на этот вопрос. Попробуйте спросить по-другому или добавьте вопрос командой /new.")
        
