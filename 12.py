# Порог схожести для определения похожего вопроса (от 0 до 1)
SIMILARITY_THRESHOLD = 0.7


class Form(StatesGroup):
    question = State()
    answers = State()

async def find_best_match(query: str) -> str:
    """
    Находит наиболее подходящий вопрос в базе знаний.

    Args:
        query: Вопрос пользователя.

    Returns:
        Ответ на наиболее подходящий вопрос или None, если не найдено.
    """
    best_match = None
    best_similarity = 0

    for question, answer in knowledge_base.items():
        similarity = difflib.SequenceMatcher(None, query.lower(), question.lower()).ratio()
        if similarity > best_similarity:
            best_similarity = similarity
            best_match = answer

    if best_similarity >= SIMILARITY_THRESHOLD:
        return best_match
    else:
        return None

